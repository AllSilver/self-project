	`file_path` VARCHAR(30) NOT NULL,
	`b_idx` INT(11) NOT NULL,
	PRIMARY KEY (`f_idx`)
)ENGINE=MyISAM AUTO_INCREMENT=1203 DEFAULT CHARSET=utf8




